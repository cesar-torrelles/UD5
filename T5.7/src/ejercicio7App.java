
public class ejercicio7App {
	
	public static void main(String[] args) {

		//declaramos la variable "A" con el valor inicial 1
		int A = 1;
		
		//realizamos un loop while para que imprima todos los valores de "A" al 100  incluido
		// y que vaya imprimiendo en consola en una linea nueva cada valor hasta llegar a 100 
		
		while(A<=100) {
			
			System.out.println(A);
			
		A++;
			
		
		}
	}
		
}


